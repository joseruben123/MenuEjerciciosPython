num2 = int(numero2) #Esta variable convierte el string numero2 a entero y lo guarda en una nueva variable.

sumaNums = num1 + num2 #Esta variable suma los dos numeros y los guarda en una nueva variable.

strDecimal = "1800.35" # guardamos en variable un string

decimal = float(strDecimal) #Convertimos el string a un valor de tipo decimal

"""
		A continuacion las siguientes lineas nos permitiran imprimir las variables creadas
		
"""
print(Edad) #Esta linea imprime la concatenaci√≥n de los dos numeros.

print(sumaNums) #Esta linea imprime la suma de los 2 numeros guardados previamente.


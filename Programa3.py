"""
	Este programa nos ayudara a unir el valor de dos variables de tipo strings en una sola

"""

numero1 = "19" # Esta linea guarda un valor tipo string en una variable

numero2 = "98" # Esta linea guarda un valor tipo string en una variable

Edad = numero1 + numero2 #Esta variable concatena, los dos strings.

